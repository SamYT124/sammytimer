async function copyToClipboard(text) {
  try {
    await navigator.clipboard.writeText(text);
    alert('Text successfully copied to clipboard!');
  } catch (err) {
    alert('Failed to copy text: ', err);
  }
}

function copyLastFive() {
    if (solves.length < 5) {
        alert("You need at least 5 solves to copy!");
        return;
    }

    const lastFiveSolves = fullsolves.slice(-5);
    const lastFiveTimes = solves.slice(-5);
    const calculatedAo5 = calculateAverage(lastFiveTimes).toFixed(2);
    const sortedTimes = [...lastFiveTimes].sort((a, b) => a - b);
    const bestTime = sortedTimes[0];
    const worstTime = sortedTimes[sortedTimes.length - 1];

    let hasBracketedBest = false;
    let hasBracketedWorst = false;
    const formattedSolvesString = lastFiveSolves.map((solve, index) => {
        let timeString = solve.time.toFixed(2);
        if (solve.time === bestTime && !hasBracketedBest) {
            timeString = `(${timeString})`;
            hasBracketedBest = true;
        } else if (solve.time === worstTime && !hasBracketedWorst) {
            timeString = `(${timeString})`;
            hasBracketedWorst = true;
        }
        
        return `${index + 1}. ${timeString}   ${solve.scramble}`;
    }).join("\n");

    const finalClipboardText = `Generated by SammyTimer\nAo5: ${calculatedAo5}\n\nSolves:\n${formattedSolvesString}`;

    copyToClipboard(finalClipboardText);
}

