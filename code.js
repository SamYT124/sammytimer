function areOpposite(face1, face2) {
    const opposites = { 'U': 'D', 'D': 'U', 'R': 'L', 'L': 'R', 'F': 'B', 'B': 'F' };
    return opposites[face1] === face2;
}

function generate3x3Scramble(length = 20) {
    const faces = ['U', 'D', 'R', 'L', 'F', 'B'];
    const modifiers = ['', "'", '2'];
    const scramble = [];
    
    let lastFace = '';
    let secondLastFace = '';

    for (let i = 0; i < length; i++) {
        let chosenFace;

        do {
            chosenFace = faces[Math.floor(Math.random() * faces.length)];
        } while (
            chosenFace === lastFace || 
            (chosenFace === secondLastFace && areOpposite(chosenFace, lastFace))
        );

        const chosenModifier = modifiers[Math.floor(Math.random() * modifiers.length)];
        scramble.push(chosenFace + chosenModifier);

        secondLastFace = lastFace;
        lastFace = chosenFace;
    }

    return scramble.join(' ');
}

