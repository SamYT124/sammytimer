function generateSq1Scramble(length = 14) {
    const validMoves = [0, 1, 2, 3, 4, 5, 6, -1, -2, -3, -4, -5];
    const scramble = [];

    for (let i = 0; i < length; i++) {
        let top, bottom;
        
        do {
            top = validChoice(validMoves);
            bottom = validChoice(validMoves);
        } while (top === 0 && bottom === 0);

        const topStr = top === 0 ? "0" : top;
        const bottomStr = bottom === 0 ? "0" : bottom;

        scramble.push(`(${topStr}, ${bottomStr}) /`);
    }

    return scramble.join(' ');
}

function validChoice(arr) {
    return arr[Math.floor(Math.random() * arr.length)];
}

